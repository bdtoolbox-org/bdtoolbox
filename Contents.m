% BDTOOLBOX - Brain Dynamics Toolbox
%
% The Brain Dynamics Toolbox (https://bdtoolbox.org) is an open-source
% toolbox for simulating dynamical systems in neuroscience and related
% fields. See the "Handbook for the Brain Dynamics Toolbox" for more.
% 
% Graphical Application
%   bdGUI           - Brain Dynamics Toolbox Graphical User Interface.
%
% Command-Line Tools
%   bdSolve         - Solve an initial-value problem.
%   bdEvolve        - Evolve an initial-value problem.
%   bdEval          - Evaluate the solution of a differential equation.
%
%   bdGetLag        - Read a lag parameter from a system structure
%   bdGetPar        - Read a parameter from a system structure
%   bdGetValue      - Read a named value from a pardef/vardef/lagdef cell array.
%   bdGetValues     - Read all values from a pardef/vardef/lagdef cell array. 
%   bdGetVar        - Read a state variable from a system structure
%
%   bdSetLag        - Write a lag parameter value into a system structure
%   bdSetPar        - Write a parameter value into a system structure
%   bdSetValue      - Write a value in a pardef/vardef/lagdef cell array.
%   bdSetValues     - Write all values in a pardef/vardef/lagdef cell array.
%   bdSetVar        - Write a state variable in a system structure
%
%   bdSysCheck      - Verifies the format of a system struct.
